<h2>API</h2>

<br />
<p>
The status page uses an API to contact the backend for information about the status
and the servers being listed. This backend is open for other developers to be used
in their own applications.
</p>

<br />

<p>
The API has recently changed, it doesn't work as a pure HTTP API any longer, instead
it will use WebSockets. We're thinking about making a fallback server for backwards
compatibility, but for now, the API documentation is not available.
</p>

